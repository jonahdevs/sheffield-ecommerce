        function stkWaiting() {
            return {
                timeLeft: 60,
                checkoutRequestId: null,
                interval: null,

                init() {
                    // Listen for Livewire event to open modal
                    Livewire.on('stk-push-initiated', ({
                        checkoutRequestId
                    }) => {
                        this.checkoutRequestId = checkoutRequestId;
                        $flux.modal('stk-waiting').show();
                        this.startCountdown();
                    });
                },

                start(id) {
                    this.checkoutRequestId = id;
                    this.startCountdown();
                },

                startCountdown() {
                    this.timeLeft = 60;
                    this.interval = setInterval(() => {
                        this.timeLeft--;
                        if (this.timeLeft <= 0) {
                            clearInterval(this.interval);
                            // Redirect to order status page — let webhook update the order
                            window.location.href = '<?php echo e(route('customer.orders.index')); ?>';
                        }
                    }, 1000);
                },
            };
        }
