    }

    public function save(): void
    {
        try {
            $isEditing = (bool) $this->form->taxClass;
            $isEditing ? $this->form->update() : $this->form->store();

            $this->form->reset();
            unset($this->taxClasses);
            Flux::modal('tax-class-modal')->close();
